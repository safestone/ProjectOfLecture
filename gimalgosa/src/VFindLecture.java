
public class VFindLecture {
	private String Root;
	private String Subject;
	private String lectureNum;
	private String SelectNum;
	private String Select;
	private String SelectEnglish;
	
	public String getRoot() {
		return Root;
	}

	public void setRoot(String root) {
		this.Root = root;
	}

	public String getSubject() {
		return Subject;
	}
	
	public void setSubject(String subject) {
		this.Subject = subject;
	}
	
	public String getLectureNum() {
		return lectureNum;
	}

	public void setLectureNum(String lectureNum) {
		this.lectureNum = lectureNum;
	}

	public String getSelectNum() {
		return SelectNum;
	}

	public void setSelectNum(String num) {
		this.SelectNum = num;
	}

	public String getSelect() {
		return Select;
	}

	public void setSelect(String select) {
		this.Select = select;
	}

	public String getSelectEnglish() {
		return SelectEnglish;
	}

	public void setSelectEnglish(String selectEnglish) {
		this.SelectEnglish = selectEnglish;
	}



}
